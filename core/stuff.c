int get_choice(int min, int max)
{
    char choice[TEMPCHAR_LENGTH];
    gets(choice);
    int n = atoi(choice);
    if (!(n >= min && n <= max))
    {
        puts("Unexpected argument");
        return -1;
    }
    return n;
}